use ink_env::{
    call::{
        utils::{
            ReturnType,
            Set,
        },
        CallBuilder,
        Execution,
        ExecutionInput,
    },
    Environment,
};

/// Trait used as bound for the outputs of ink! trait definition messages.
///
/// # Note
///
/// Indicates that a value of type `T` or an equivalent `CallBuilder`
/// instantiation of an ink! message returning a value of type `T` are
/// returned by the respective ink! trait message.
///
/// The bounds are automatically generated by the `#[ink::trait_definition]`
/// procedural macro.
pub trait ImpliesReturn<T> {}

impl<T> ImpliesReturn<T> for T {}
impl<T, E, CallType, Args, Abi> ImpliesReturn<T>
    for CallBuilder<E, Set<CallType>, Set<ExecutionInput<Args, Abi>>, Set<ReturnType<T>>>
where
    E: Environment,
{
}

impl<E, CallType, Args, Abi> ImpliesReturn<()>
    for CallBuilder<E, Set<CallType>, Set<ExecutionInput<Args, Abi>>, Set<()>>
where
    E: Environment,
{
}

impl<Args, RetType, Abi> ImpliesReturn<RetType> for Execution<Args, RetType, Abi> {}
